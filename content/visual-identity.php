    <h3 class="heading-2">3.1. Logo System</h3>
  </section>
    <section id="ideogram">
      <h4 class="heading-3">3.1.1. Ideogram</h4>
      <p>The Ideogram is the visual representation of Polyfen's brand in its most basic and essential form. It's reserved for use exclusively on reduced sizes and square or circle-shaped canvases.</p>
      <p>Polyfen's Ideogram represents the conjunction of two worlds, the world of business and the creative realm. The left hemisphere of the brain (analytical), and the right hemisphere (emotional).</p>
    </section>
    <section id="emblem">
      <h4 class="heading-3">3.1.2. Emblem</h4>
      <p>The Emblem is the preferred visual representation of the Polyfen brand. It must be the first choice when chosing a logo variation, provided that the canvas is rectangular and the logo can be placed at an adequate size.</p>
    </section>
    <section id="wordmark">
      <h4 class="heading-3">3.1.3. Wordmark</h4>
      <p>The Wordmark is the most basic logo variation for textual use. It's reserved exclusively for cases when the brand name must be written at sizes too small to fit the Emblem comforatbly.</p>
    </section>

